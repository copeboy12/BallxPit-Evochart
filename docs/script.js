const categories = ["All", "Base Game", "Regal Update", "Shadow Update", "Multi-step"];

const state = {
  query: "",
  category: "All",
  selected: new Set(),
  strict: false,
  pinned: null
};

const byId = new Map(items.map(item => [item.id, item]));
const $ = selector => document.querySelector(selector);

const categoryTabs = $("#categoryTabs");
const itemGrid = $("#itemGrid");
const recipeGrid = $("#recipeGrid");
const matchCount = $("#matchCount");
const searchInput = $("#searchInput");
const selectedCount = $("#selectedCount");
const detailPanel = $("#detailPanel");
const strictButton = $("#strictButton");

function initials(name) {
  return name.split(/\s+/).map(p => p[0]).join("").slice(0, 2).toUpperCase();
}

function iconHtml(item, className = "icon") {
  const safeName = item?.name || "Unknown";
  const image = item?.image || `assets/icons/ballxpit/${slugify(safeName)}.png`;
  return `<span class="${className} fallback" data-label="${initials(safeName)}"><img src="${image}" alt="${safeName}" loading="lazy" onerror="this.remove(); this.parentElement.classList.add('show-fallback')"></span>`;
}

function slugify(text) {
  return String(text).toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function recipeCategory(recipe) {
  if (recipe.inputs.length > 2 || recipe.inputs.some(id => recipes.some(r => r.resultId === id))) return "Multi-step";
  return recipe.category;
}

function fullText(recipe) {
  const inputNames = recipe.inputs.map(id => byId.get(id)?.name || id);
  return [recipe.result, recipe.category, recipeCategory(recipe), recipe.note, ...inputNames].join(" ").toLowerCase();
}

function visibleRecipes() {
  const q = state.query.trim().toLowerCase();
  return recipes.filter(recipe => {
    const category = recipeCategory(recipe);
    const categoryOk = state.category === "All" || recipe.category === state.category || category === state.category;
    const searchOk = !q || fullText(recipe).includes(q);
    const selected = [...state.selected];
    const selectedOk = !selected.length
      ? true
      : state.strict
        ? selected.every(id => recipe.inputs.includes(id) || recipe.resultId === id)
        : selected.some(id => recipe.inputs.includes(id) || recipe.resultId === id);
    return categoryOk && searchOk && selectedOk;
  }).sort((a, b) => a.result.localeCompare(b.result));
}

function renderTabs() {
  categoryTabs.innerHTML = categories.map(cat => `
    <button class="tab ${state.category === cat ? "active" : ""}" data-category="${cat}">${cat}</button>
  `).join("");
}

function renderItems() {
  const usedIds = new Set(recipes.flatMap(r => [...r.inputs, r.resultId]));
  const shown = items.filter(item => usedIds.has(item.id)).sort((a,b) => a.name.localeCompare(b.name));
  itemGrid.innerHTML = shown.map(item => `
    <button class="chip ${state.selected.has(item.id) ? "selected" : ""}" data-id="${item.id}">
      ${iconHtml(item)}
      <span>${item.name}</span>
    </button>
  `).join("");
  selectedCount.textContent = `${state.selected.size} selected`;
}

function renderRecipes() {
  const list = visibleRecipes();
  recipeGrid.innerHTML = list.map((recipe, index) => {
    const inputs = recipe.inputs.map(id => {
      const item = byId.get(id) || { id, name: id };
      return `<span class="mini-item">${iconHtml(item)}<span>${item.name}</span></span>`;
    }).join(`<span class="operator">+</span>`);

    const result = { name: recipe.result, image: `assets/icons/ballxpit/${recipe.resultId}.png` };
    return `
      <button class="recipe-card" data-index="${index}">
        <div class="recipe-top">
          <span class="pill">${recipeCategory(recipe)}</span>
          <span class="pill muted">${recipe.inputs.length} input${recipe.inputs.length > 1 ? "s" : ""}</span>
        </div>
        <div class="recipe-line">${inputs}<span class="operator equals">=</span><span class="mini-item result">${iconHtml(result, "icon result-icon")}<span>${recipe.result}</span></span></div>
        ${recipe.note ? `<p>${recipe.note}</p>` : ""}
      </button>
    `;
  }).join("") || `<div class="empty">No matches. Try clearing filters or searching a different ball name.</div>`;

  matchCount.textContent = `${list.length} match${list.length === 1 ? "" : "es"}`;
}

function renderDetails(recipe = state.pinned) {
  if (!recipe) {
    detailPanel.innerHTML = `
      <p class="eyebrow">Details</p>
      <h2>Select an evolution</h2>
      <p>Click a recipe to pin the full combo here.</p>
    `;
    return;
  }
  const inputNames = recipe.inputs.map(id => byId.get(id)?.name || id).join(" + ");
  detailPanel.innerHTML = `
    <p class="eyebrow">${recipeCategory(recipe)}</p>
    <h2>${recipe.result}</h2>
    <div class="detail-combo">${inputNames} <strong>=</strong> ${recipe.result}</div>
    <p>${recipe.note || "Real evolution recipe."}</p>
  `;
}

function render() {
  renderTabs();
  renderItems();
  renderRecipes();
  renderDetails();
}

categoryTabs.addEventListener("click", event => {
  const button = event.target.closest("[data-category]");
  if (!button) return;
  state.category = button.dataset.category;
  render();
});

itemGrid.addEventListener("click", event => {
  const button = event.target.closest("[data-id]");
  if (!button) return;
  const id = button.dataset.id;
  state.selected.has(id) ? state.selected.delete(id) : state.selected.add(id);
  renderItems();
  renderRecipes();
});

recipeGrid.addEventListener("click", event => {
  const card = event.target.closest("[data-index]");
  if (!card) return;
  state.pinned = visibleRecipes()[Number(card.dataset.index)];
  renderDetails();
});

searchInput.addEventListener("input", event => {
  state.query = event.target.value;
  renderRecipes();
});

strictButton.addEventListener("click", () => {
  state.strict = !state.strict;
  strictButton.classList.toggle("active", state.strict);
  strictButton.textContent = state.strict ? "Match all selected" : "Match any selected";
  renderRecipes();
});

$("#resetButton").addEventListener("click", () => {
  state.query = "";
  state.category = "All";
  state.selected.clear();
  state.strict = false;
  state.pinned = null;
  searchInput.value = "";
  strictButton.classList.remove("active");
  strictButton.textContent = "Match any selected";
  render();
});

render();
